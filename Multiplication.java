class Multiplication {

public static void main(String args[]){
    int a,b,c;
    a = 5;
    b = 3;
    c = a * b;
    System.out.println("Result is " + c);
    
    float p,q,r;
    p = 4.9f;
    q = 3.8f;
    r = p * q;
    System.out.println("Result is " + r);

    double x, y, z;
    x = 4.9;
    y = 3.8;
    z = x * y;
    System.out.println("Result is " + z);
}
}