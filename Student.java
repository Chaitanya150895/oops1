class Student {

String id;
String name;
String age;


public String getId(){
return id;
}

public String getName(){
return name;
}

public String getAge(){
return age;
}

public void setId(String newId){
    id = newId;

}

public void setName(String newName){
    name = newName;

}

public void setAge(String newAge){
age = newAge;

}

}